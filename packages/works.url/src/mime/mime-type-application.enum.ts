/**
 * Application mime types.
 */
export enum ZMimeTypeApplication {
  /**
   * JSON data.
   */
  JSON = 'application/json',

  /**
   * The unknown type.
   *
   * Used for raw byte data.
   */
  OctetStream = 'application/octet-stream',

  /**
   * Compressed zip stream.
   */
  Zip = 'application/zip'
}
